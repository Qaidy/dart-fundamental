void main(List<String> args) {
  // operator aritmatika
  var a = 5;
  var b = 2;

  print(a + b);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  // operator relasi perbandingan
  print(a > b);
  print(a < b);
  print(a >= b);
  print(a <= b);
  print(a == b);
  print(a != b);

  // operator increment dan decrement
  print(a++);
  print(a--);
  print(++a);
  print(--a);

  // operator logika
  print(a > b && a < b);
  print(a > b || a < b);
  print(!(a > b));
}
